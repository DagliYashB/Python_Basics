# Mutable
# Not Create Duplicate [ Don't Repet ]
# Unordered
dic = {
    "key" : "value",
    "name" : "yash",
    "surname" : "dagli",
    "age" : 20,
    "learning" : "coding",
    "language" : "python",
    "marks" : 94.9,
}

print(dic)
print(type(dic))
print(dic["name"])
print(dic["age"])
print(dic["marks"])
print(dic["language"])

# Change Value
dic["name"] = "Smit"
dic["surname"] = "Dagli"
print(dic)

# Null Dictionary
null_dic = {}
null_dic ["name"] = "YashDagli"
print(null_dic)